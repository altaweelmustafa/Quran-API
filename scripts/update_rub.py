def update_rub():
    # Build lookup: (surah, ayah) -> rub_number
    rub_map = {}
    for idx, (sura, aya) in enumerate(RUB_BOUNDARIES, start=1):
        rub_map[(sura, aya)] = idx

    db = SessionLocal()
    try:
        # Get all ayat ordered
        ayat = db.query(Ayah).order_by(Ayah.surah_id, Ayah.ayah_number).all()

        current_rub = 1
        for ayah in ayat:
            key = (ayah.surah_id, ayah.ayah_number)
            if key in rub_map:
                current_rub = rub_map[key]
            ayah.rub_number = current_rub

        db.commit()
        print(f"Successfully updated rub numbers for {len(ayat)} ayat.")
    except Exception as e:
        db.rollback()
        print(f"Error: {e}")
        raise
    finally:
        db.close()

if __name__ == "__main__":
    update_rub()
